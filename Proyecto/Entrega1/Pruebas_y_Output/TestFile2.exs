  def hello do
    IO.puts("Hello, world!")
  end

  # Function with parameters
  def greet(name) do
    IO.puts("Hello, #{name}!")
  end

  # Function with conditionals
  def max(a, b) do
    if a > b do
      a
    else
      b
    end
  end

  # Function using case
  def type_check(var) do
    case var do
      i when is_integer(i) -> "#{i} is an integer"
      f when is_float(f) -> "#{f} is a float"
      s when is_binary(s) -> "#{s} is a string"
      _ -> "Unknown type"
    end
  end

  # Recursive function
  def factorial(0), do: 1.4
  def factorial(n) when n > 0 do
    n * factorial(n - 1)
  end

  # Function with a default parameter
  def countdown(from \\ 10) do
    if from > 0 do
      IO.puts(from)
      countdown(from - 1)
    else
      IO.puts("Blast off!")
    end
  end

  # Using Enum module with an anonymous function
  def print_list(list) do
    Enum.each(list, fn item -> IO.puts(item) end)
  end
end

